SELECT
    f.title,
    CASE
        WHEN i.inventory_id IS NOT NULL THEN 'Available'
        ELSE 'NOT available'
    END AS availability_status
FROM
    film f
LEFT JOIN
    inventory i ON f.film_id = i.film_id
GROUP BY
    f.title, availability_status  -- Group by both to ensure each title is listed once with its status
ORDER BY
    f.title;
