SELECT
    f.title,
    s.store_id,
    COUNT(i.inventory_id) AS inventory_count
FROM
    film f
JOIN
    inventory i ON f.film_id = i.film_id
JOIN
    store s ON i.store_id = s.store_id
WHERE
    f.title = 'ACADEMY DINOSAUR' AND s.store_id = 1
GROUP BY
    f.title, s.store_id;

